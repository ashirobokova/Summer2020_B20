package Day04_Variables;

public class Concate_Add {
    public static void main(String[] args) {
        // addition: number+numbe
        System.out.println(12+13); // sum 12+13=45
        System.out.println('A' + 2);// 65 in ascii table
        System.out.println('A' + 'B'); // 65+66=131
        // Concatination: append
        System.out.println("12" + 13); //1213 "12" is String
        System.out.println("A" + 2); // A2 "A" is String
        System.out.println("Gender: " + 'M');
        System.out.println("Tax: "+3.5 + '%');
        System.out.println(3.5 + '%'+"Tax");
           }
}
