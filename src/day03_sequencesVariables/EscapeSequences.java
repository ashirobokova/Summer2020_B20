package day03_sequencesVariables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello World \n\tMy name is Alena ");
        System.out.println("\tI am in Chicago\n");
        System.out.println("\\"); // prints me one back slash
        System.out.println("/"); // prints me one front slash
        System.out.println("\\\\"); // prints me two back slash
        System.out.println("My name is \'Alena\'"); // 'Alena'
        System.out.println("My name is 'Alena'"); // 'Alena'
        System.out.println("I'm \"Alena\""); // "Alena" giving me a double quotes


    }


}
